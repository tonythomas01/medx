    private void viewQueueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewQueueButtonActionPerformed
        // TODO add your handling code here:
        try {
            JDBCConnections j1 = new JDBCConnections();
            String[] PatientNames = j1.getPatientNames( this.DoctorId );
            patientComboBox.removeAllItems();
            DefaultComboBoxModel PatientListModel = new DefaultComboBoxModel(PatientNames);
            patientComboBox.setModel( PatientListModel );
            ResultSet rs = j1.selectParticularPatient( patientComboBox.getSelectedItem().toString() );
            jTextField1.setText( rs.getString( "p_id" ) );
            jTextField2.setText( rs.getString( "p_gender" ) );
            CurrentAppoinmentId = j1.getAppoinmentId( DoctorId, rs.getInt( "p_id" )  );        
            appoinmentId.setText( CurrentAppoinmentId.toString() );
        }catch ( Exception e ) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_viewQueueButtonActionPerformed

